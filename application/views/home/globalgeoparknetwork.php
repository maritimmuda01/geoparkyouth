        <p>
        The Global Geoparks Network (GGN), is a non-profit International Association officially established in 2014 subject to French legislation. The Global Geoparks Network is the official partner of UNESCO for the operation of the UNESCO Global Geoparks.</p>

        <p>The GGN was initially founded in 2004 as an international partnership developed under the umbrella of UNESCO and serves to develop models of best practice and set quality-standards for territories that integrate the protection preservation of Earth heritage sites in a strategy for regional sustainable economic development.
        </p>

        <p>
          The bottom-up approach is becoming increasingly popular through the positive and effective running of the GGN under UNESCO’s ad hoc support with individual Member States as appropriate.
        </p>

        <p>
        Networking and collaboration among Global Geoparks is an important component of the Global Geoparks Network. The Global Geoparks Network promotes networking on a regional basis. The GGN prioritizes the creation of similar Regional Networks, reflecting local conditions, elsewhere in the world.</p>

        <p>
          <ul>
          <li>For Global Geoparks in Asia – Pacific the Asia-Pacific Geoparks Network (APGN) acts as the Regional Network of the GGN.</li>
          <li>For Global Geoparks in Europe, the European Geoparks Network (EGN) acts as the Regional Network of the GGN.</li>
          <li>
            For Global Geoparks in Latin America and Caribbean, the Latin American and Caribbean Geoparks Network acts as the as the Regional Network of the GGN.
          </li>
        </ul>
        </p>

        The objectives of the Global Geoparks Network are:

        <ul>
          <li>
        to promote the equitable geographical establishment, development and professional management of Global Geoparks, 
          </li>
          <li>
          to advance knowledge and understanding of the nature, function and role of Global Geoparks;
        </li>
        <li>
          to assist local communities to value their natural and cultural heritage;
        </li>
        <li>
          to preserve Earth heritage for present and future generations;
        </li>
        <li>
          to educate and teach the broad public about issues in geo-sciences and their relation with environmental matters and natural hazards.
        </li>
        <li>
          to ensure sustainable socio-economic and cultural development based on the natural (or geological) system 
        </li>
        <li>
          to foster multi-cultural links between heritage and conservation and the maintenance of geological and cultural diversity, using participatory schemes of partnership and management;
        </li>
        <li>
          to promote joint initiatives between Global Geoparks (e.g. communication, publications, exchange of information, twinning).
        </li>
      </ul>
        <br>

        The Global Geoparks Network:

        <br>
        <ul>         
          <li>
            establishes ethical standards which must be adopted and respected by Global Geoparks and Global Geopark professionals;
          </li>
          <li>
            organizes co-operation and mutual assistance between Global Geoparks and between Global Geopark professionals;
          </li>
          <li>
            initiates and co-ordinates thematic Working Groups which will foster international co-operation is a variety of issues related with Geopark operation and activities;
          </li>
          <li>
            represents, advances, and disseminates knowledge in Geodiversity management and other disciplines related to studies in Geo-conservation, Geo-tourism, Geo-education and/or the management and activities of Global Geoparks.
          </li>
        </ul>
